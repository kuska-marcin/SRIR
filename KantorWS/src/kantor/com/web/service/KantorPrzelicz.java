package kantor.com.web.service;

public class KantorPrzelicz {
	public double PLNnaEUR(double value) {
		return (value/4.2);
	}
 
	public double EURnaPLN(double value) {
		return (value*4.3);
	}
	
	public double PLNnaUSD(double value) {
		return (value/3.5);
	}
 
	public double USDnaPLN(double value) {
		return (value*3.7);
	}
}
